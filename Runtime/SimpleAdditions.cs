using System.Collections;
using System.Collections.Generic;
using UnityEngine;

namespace PackageTester.Runtime
{
    public static class SimpleAdditions
    {
        public static int AddIntegers(int x, int y)
        {
            return x + y;
        }

        public static float AddFloats(float x, float y)
        {
            return x + y;
        }
    }
}